function [lhs, rhs] = static_resid(y, x, params)
T = NaN(1, 1);
lhs = NaN(9, 1);
rhs = NaN(9, 1);
T(1) = 1/params(3);
lhs(1) = y(1);
rhs(1) = y(1)*params(1)-T(1)*(y(6)-y(5)-params(8)*y(7));
lhs(2) = y(2);
rhs(2) = y(3)+y(9);
lhs(3) = y(4);
rhs(3) = y(2)*params(11)+y(1)*(1-params(11));
lhs(4) = y(9);
rhs(4) = y(3)*(-params(4))+y(8);
lhs(5) = y(3);
rhs(5) = y(1)*params(3)+y(9)*params(4);
lhs(6) = y(5);
rhs(6) = params(1)*y(5)+(params(3)+params(4))/params(5)*(y(4)-y(9));
lhs(7) = y(6);
rhs(7) = y(5)*params(6)+y(4)*params(7)+x(2);
lhs(8) = y(7);
rhs(8) = params(8)*y(7)+x(1);
lhs(9) = y(8);
rhs(9) = y(8)*params(10)+x(3);
end
